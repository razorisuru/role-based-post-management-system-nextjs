export function NavLinks({ canViewUsers, canManageSettings }) {
  const pathname = usePathname()

  const isActive = (href) => {
    if (href === '/dashboard') {
      return pathname === '/dashboard'
    }
    return pathname.startsWith(href)
  }

  const filteredItems = navItems.filter((item) => {
    if (item.permission === 'users') return canViewUsers
    if (item.permission === 'settings') return canManageSettings
    return true
  })

  return (
    <div className="flex items-center gap-1">
      {filteredItems.map((item) => {
        const active = isActive(item.href)
        return (
          <Link
            key={item.href}
            href={item.href}
            className={cn(
              "relative flex items-center gap-2 px-3 py-2 rounded-lg text-sm font-medium transition-all duration-200",
              active 
                ? "text-primary bg-primary/10" 
                : "text-muted-foreground hover:text-foreground hover:bg-accent/50"
            )}
          >
            {item.icon}
            <span>{item.title}</span>
            {active && (
              <span className="absolute -bottom-[17px] left-1/2 -translate-x-1/2 w-8 h-0.5 bg-primary rounded-full" />
            )}
          </Link>
        )
      })}
    </div>
  )
}
